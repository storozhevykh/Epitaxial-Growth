
public struct ThreeAtomsPos
{
    public float Atom0_x;
    public float Atom0_y;
    public float Atom0_z;

    public float Atom1_x;
    public float Atom1_y;
    public float Atom1_z;

    public float Atom2_x;
    public float Atom2_y;
    public float Atom2_z;

    public ThreeAtomsPos(float Atom0_x, float Atom0_y, float Atom0_z, float Atom1_x, float Atom1_y, float Atom1_z, float Atom2_x, float Atom2_y, float Atom2_z)
    {
        this.Atom0_x = Atom0_x;
        this.Atom0_y = Atom0_y;
        this.Atom0_z = Atom0_z;
        this.Atom1_x = Atom1_x;
        this.Atom1_y = Atom1_y;
        this.Atom1_z = Atom1_z;
        this.Atom2_x = Atom2_x;
        this.Atom2_y = Atom2_y;
        this.Atom2_z = Atom2_z;
    }
}
